#Criar pequenos exemplos de funções lambda para cálculos rápidos.

soma = lambda x, y: x + y
print("Soma de 5 e 3:", soma(5, 3))

subtracao = lambda x, y: x - y
print("Subtração de 5 e 3:", subtracao(5, 3))

multiplicacao = lambda x, y: x * y
print("Multiplicação de 5 e 3:", multiplicacao(5, 3))

divisao = lambda x, y: x / y
print("Divisão de 5 e 3:", divisao(5, 3))

