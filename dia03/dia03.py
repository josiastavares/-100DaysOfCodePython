#Criação de variáveis e fazer calculos com elas

valor1 = 10
valor2 = 3

soma = valor1 + valor2
print(soma)

subtracao = valor1 - valor2
print(subtracao)

multiplicacao = valor1 * valor2
print(multiplicacao)

divisao = valor1 / valor2
print(divisao)

potencia = valor1 ** valor2
print(potencia)

resto = valor1 % valor2
print(resto)